"use client";

import { useState } from "react";
import { useRouter } from "next/navigation";
import { authClient } from "@/lib/auth-client";
import { FormProvider, useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import {
  Card,
  CardContent,
  CardDescription,
  CardHeader,
  CardTitle,
} from "@/components/ui/card";
import {
  Field,
  FieldLabel,
  FieldDescription,
  FieldGroup,
} from "@/components/ui/field";
import { toast } from "sonner";
import { sendWelcomeEmail, resendVerificationEmail } from "../lib/auth-api";
import { PasswordInput } from "@/features/auth/components/password-input";
import { cn } from "@/lib/utils";
import type { RegisterFormProps } from "../types/auth";
import { registerFormSchema, type RegisterFormValues } from "../lib/schemas";

// ============================================
// Register Form Component
// ============================================

export function RegisterForm({
  defaultEmail = "",
  defaultName = "",
  showLoginLink = true,
  className,
}: RegisterFormProps) {
  const router = useRouter();
  const [isLoading, setIsLoading] = useState(false);

  const form = useForm<RegisterFormValues>({
    resolver: zodResolver(registerFormSchema),
    defaultValues: {
      name: defaultName,
      email: defaultEmail,
      password: "",
      confirmPassword: "",
    },
  });

  const { setError } = form;

  const handleSubmit = async (values: RegisterFormValues) => {
    setIsLoading(true);

    try {
      const { error } = await authClient.signUp.email({
        name: values.name,
        email: values.email,
        password: values.password,
      });

      if (error) {
        setError("root", {
          message: error.message || "Failed to create account",
        });
        toast.error(error.message || "Failed to create account");
        return;
      }

      toast.success(
        "Account created! Please check your email to verify your account.",
      );
      const [verificationResult, welcomeResult] = await Promise.all([
        resendVerificationEmail(values.email),
        sendWelcomeEmail(values.name, values.email),
      ]);

      if (verificationResult?.error) {
        console.error(
          "[Signup] Failed to send verification email:",
          verificationResult.error,
        );
        toast.error(
          verificationResult.error ||
            "Account created but verification email failed. Please use the resend form.",
        );
      } else {
        console.log(
          "[Signup] Verification email sent successfully to:",
          values.email,
        );
      }

      if (!welcomeResult.success) {
        console.error(
          "[Signup] Failed to send welcome email:",
          welcomeResult.error,
        );
      } else {
        console.log(
          "[Signup] Welcome email sent successfully to:",
          values.email,
        );
      }

      toast.success(
        "Account created! Please check your email to verify your account.",
      );
      router.push("/verification-pending");
    } catch {
      setError("root", { message: "An unexpected error occurred" });
      toast.error("An unexpected error occurred");
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <Card className={cn("w-full max-w-md", className)}>
      <CardHeader>
        <CardTitle>Create an account</CardTitle>
        <CardDescription>
          Enter your information below to create your account
        </CardDescription>
      </CardHeader>
      <CardContent>
        <FormProvider {...form}>
          <form onSubmit={form.handleSubmit(handleSubmit)}>
            <FieldGroup>
              <Field>
                <FieldLabel htmlFor="name">Full Name</FieldLabel>
                <Input
                  id="name"
                  type="text"
                  placeholder="John Doe"
                  disabled={isLoading}
                  {...form.register("name")}
                />
                {form.formState.errors.name && (
                  <p className="text-sm text-red-500">
                    {form.formState.errors.name.message}
                  </p>
                )}
              </Field>

              <Field>
                <FieldLabel htmlFor="email">Email</FieldLabel>
                <Input
                  id="email"
                  type="email"
                  placeholder="m@example.com"
                  disabled={isLoading}
                  {...form.register("email")}
                />
                {form.formState.errors.email && (
                  <p className="text-sm text-red-500">
                    {form.formState.errors.email.message}
                  </p>
                )}
              </Field>

              <Field>
                <PasswordInput
                  name="password"
                  label="Password"
                  placeholder="Create a password"
                  disabled={isLoading}
                  form={form}
                  showStrength
                />
              </Field>

              <Field>
                <PasswordInput
                  name="confirmPassword"
                  label="Confirm Password"
                  placeholder="Confirm your password"
                  disabled={isLoading}
                  form={form}
                />
                {form.watch("confirmPassword") &&
                  form.watch("password") !== form.watch("confirmPassword") && (
                    <p className="text-sm text-red-500">
                      Passwords do not match
                    </p>
                  )}
                {form.watch("confirmPassword") &&
                  form.watch("password") === form.watch("confirmPassword") &&
                  form.watch("password").length > 0 && (
                    <p className="text-sm text-green-500">âœ“ Passwords match</p>
                  )}
              </Field>

              {form.formState.errors.root && (
                <p className="text-sm text-red-500">
                  {form.formState.errors.root.message}
                </p>
              )}

              <Field>
                <Button type="submit" disabled={isLoading}>
                  {isLoading ? "Creating account..." : "Create Account"}
                </Button>

                {showLoginLink && (
                  <FieldDescription className="px-6 text-center">
                    Already have an account?{" "}
                    <a href="/login" className="underline underline-offset-4">
                      Sign in
                    </a>
                  </FieldDescription>
                )}
              </Field>
            </FieldGroup>
          </form>
        </FormProvider>
      </CardContent>
    </Card>
  );
}
